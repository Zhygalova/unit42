void    ft_putchar(char c);
void    rush(int x, int y)
{
	int i;
	int j;
	j = 0;
	while (j < y)
 	{
		i = 0;
		while (i < x)
		{
			if (((j == 0) || (j == y - 1)) && ((i == 0) || (i == x - 1)))
				ft_putchar('o');
			else if (((j == 0) || (j == y - 1)) && ((i != 0) || (i != x - 1)))
				ft_putchar('-');
			else if (((j != 0) || (j != y - 1)) && ((i == 0) || (i == x - 1)))
				ft_putchar('|');
			else
				ft_putchar(' ');
				i++;
		}
		ft_putchar('\n');
		j++;
	}
}
